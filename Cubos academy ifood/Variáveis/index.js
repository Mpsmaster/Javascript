//Como criar uma variável
const nome = "José";
//const não aceita uma outra atribuição para a constante criada
//const tem escopo local ou de bloco
//Com aspas o compilador imprime o conteúdo da string
console.log("nome");
//sem aspas ele imprimi o conteúdo da variável
console.log(nome);
var namevar = "jhon";
namevar = "Jane"
//var aceita atribuir outro valores para a variável
//var tem escopo global e local
console.log(namevar);
let age = 36;
console.log(age)
//let tem escopo local ou de bloco